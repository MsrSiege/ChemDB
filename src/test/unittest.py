# ++---------------------------------------------------------------------------------------------------------------------++#
# ++ Imports
# ++---------------------------------------------------------------------------------------------------------------------++#
import unittest
from os import getcwd, path


# ++---------------------------------------------------------------------------------------------------------------------++#
# ++ Main unit test call
# ++---------------------------------------------------------------------------------------------------------------------++#
def RunUnitTests():
    test_loader = unittest.TestLoader()
    test_path = path.join(getcwd(), "src", "test")
    test_suite = test_loader.discover(start_dir=test_path, pattern="test_*.py")

    test_runner = unittest.TextTestRunner()
    test_runner.run(test_suite)


# +-----------------------------------------------------------------------------------------------------------------------+#
# ++ Software main entrypoint
# +-----------------------------------------------------------------------------------------------------------------------+#
if __name__ == "__main__":
    RunUnitTests()
